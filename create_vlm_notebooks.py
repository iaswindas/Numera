        "# Set your API key (from Google AI Studio)\n",
        "GEMINI_API_KEY = os.environ.get('GEMINI_API_KEY', 'your-key-here')\n",
        "genai.configure(api_key=GEMINI_API_KEY)\n",
        "\n",
        "model = genai.GenerativeModel('gemini-1.5-flash')\n",
