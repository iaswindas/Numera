# Notebook 02: Data Preparation
# ══════════════════════════════════════════════════════════════
nb02 = make_nb([
    md(["# 02 — Data Preparation for Qwen3-VL Fine-Tuning\n",
        "\n",
        "Converts Gemini auto-labels into conversation-format training data\n",
        "for Qwen3-VL QLoRA fine-tuning.\n"]),
    code([
        "# Cell 1: Load labels and build conversation pairs\n",
        "import json, glob, random\n",
        "from pathlib import Path\n",
        "\n",
        "LABELS_DIR = Path('/content/drive/MyDrive/numera-ml/data/labels')\n",
        "IMAGES_DIR = Path('/content/drive/MyDrive/numera-ml/data/page_images')\n",
        "OUTPUT_DIR = Path('/content/drive/MyDrive/numera-ml/data/training')\n",
        "OUTPUT_DIR.mkdir(parents=True, exist_ok=True)\n",
        "\n",
        "label_files = sorted(glob.glob(str(LABELS_DIR / '**/*.json'), recursive=True))\n",
        "print(f'Found {len(label_files)} labeled pages')\n",
        "\n",
        "# Build conversation-format training data\n",
        "conversations = []\n",
        "for lf in label_files:\n",
        "    label = json.loads(Path(lf).read_text())\n",
        "    img_path = str(IMAGES_DIR / Path(lf).relative_to(LABELS_DIR).with_suffix('.png'))\n",
        "\n",
        "    if not Path(img_path).exists():\n",
        "        continue\n",
        "\n",
        "    conversations.append({\n",
        "        'image': img_path,\n",
        "        'conversations': [\n",
        "            {'role': 'user', 'content': 'Extract all financial tables from this page with complete structure, zone classification, periods, currency, and values. Return JSON.'},\n",
        "            {'role': 'assistant', 'content': json.dumps(label)},\n",
        "        ]\n",
        "    })\n",
        "\n",
        "print(f'Built {len(conversations)} training conversations')\n",
    ]),
    code([
        "# Cell 2: Train/val/test split (80/10/10)\n",
        "random.shuffle(conversations)\n",
        "\n",
        "n = len(conversations)\n",
        "train = conversations[:int(n * 0.8)]\n",
        "val = conversations[int(n * 0.8):int(n * 0.9)]\n",
        "test = conversations[int(n * 0.9):]\n",
        "\n",
        "for name, data in [('train', train), ('val', val), ('test', test)]:\n",
        "    path = OUTPUT_DIR / f'{name}.json'\n",
        "    path.write_text(json.dumps(data, indent=2))\n",
        "    print(f'{name}: {len(data)} samples → {path.name}')\n",
    ]),
])

