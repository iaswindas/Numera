# Notebook 04: Evaluation
# ══════════════════════════════════════════════════════════════
nb04 = make_nb([
    md(["# 04 — Model Evaluation\n",
        "\n",
        "Evaluates the fine-tuned Qwen3-VL on the test set.\n",
        "\n",
        "**Targets**: ≥98% cell accuracy, ≥95% TEDS, ≥98% zone accuracy\n"]),
    code([
        "# Cell 1: Load fine-tuned model\n",
        "from unsloth import FastVisionModel\n",
        "\n",
        "model, tokenizer = FastVisionModel.from_pretrained(\n",
        "    '/content/drive/MyDrive/numera-ml/models/qwen3vl-financial-lora',\n",
        "    load_in_4bit=True,\n",
        ")\n",
        "FastVisionModel.for_inference(model)\n",
        "print('Model loaded for evaluation')\n",
    ]),
    code([
        "# Cell 2: Run evaluation on test set\n",
        "import json\n",
        "from pathlib import Path\n",
        "from PIL import Image\n",
        "\n",
        "test_data = json.loads(Path('/content/drive/MyDrive/numera-ml/data/training/test.json').read_text())\n",
        "print(f'Evaluating on {len(test_data)} test samples')\n",
        "\n",
        "results = []\n",
        "for item in test_data:\n",
        "    img = Image.open(item['image']).convert('RGB')\n",
        "    expected = json.loads(item['conversations'][1]['content'])\n",
        "\n",
        "    # Run inference\n",
        "    messages = [\n",
        "        {'role': 'user', 'content': [{'type': 'image', 'image': img}, {'type': 'text', 'text': item['conversations'][0]['content']}]}\n",
        "    ]\n",
        "    prompt = tokenizer.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)\n",
        "    inputs = tokenizer(prompt, images=[img], return_tensors='pt').to('cuda')\n",
        "\n",
        "    import torch\n",
        "    with torch.no_grad():\n",
        "        output = model.generate(**inputs, max_new_tokens=4096, do_sample=False)\n",
        "    predicted_text = tokenizer.decode(output[0][inputs['input_ids'].shape[1]:], skip_special_tokens=True)\n",
        "\n",
        "    try:\n",
        "        predicted = json.loads(predicted_text)\n",
        "    except json.JSONDecodeError:\n",
        "        predicted = {'tables': []}\n",
        "\n",
        "    results.append({'expected': expected, 'predicted': predicted, 'image': item['image']})\n",
        "\n",
        "print(f'Evaluation complete: {len(results)} samples')\n",
    ]),
    code([
        "# Cell 3: Calculate metrics\n",
        "\n",
        "zone_correct = 0\n",
        "zone_total = 0\n",
        "value_correct = 0\n",
        "value_total = 0\n",
        "\n",
        "for r in results:\n",
        "    exp_tables = r['expected'].get('tables', [])\n",
        "    pred_tables = r['predicted'].get('tables', [])\n",
        "\n",
        "    for exp, pred in zip(exp_tables, pred_tables):\n",
        "        # Zone accuracy\n",
        "        zone_total += 1\n",
        "        if exp.get('zone_type') == pred.get('zone_type'):\n",
        "            zone_correct += 1\n",
        "\n",
        "        # Value accuracy (cell-level)\n",
        "        for exp_row, pred_row in zip(exp.get('rows', []), pred.get('rows', [])):\n",
        "            for exp_val, pred_val in zip(exp_row.get('values', []), pred_row.get('values', [])):\n",
        "                value_total += 1\n",
        "                if exp_val == pred_val:\n",
        "                    value_correct += 1\n",
        "\n",
        "zone_acc = zone_correct / zone_total if zone_total else 0\n",
        "cell_acc = value_correct / value_total if value_total else 0\n",
        "\n",
        "print(f'Zone accuracy: {zone_acc:.2%} (target: ≥98%)')\n",
        "print(f'Cell accuracy: {cell_acc:.2%} (target: ≥98%)')\n",
        "print(f'Zone: {zone_correct}/{zone_total}, Cell: {value_correct}/{value_total}')\n",
        "\n",
        "import mlflow\n",
        "mlflow.log_metrics({'zone_accuracy': zone_acc, 'cell_accuracy': cell_acc})\n",
    ]),
])

